    public PanelVuelosCompania(java.awt.Frame parent, boolean modal) {
        super(parent, modal);
        initComponents();
        setTitle("VUELOS POR COMPAÃ‘IA");
        spnFecha.setEditor(new JSpinner.DateEditor(spnFecha, "dd/MM/yyyy"));
        listaCompanias=LogicaNegocio.getListaCompaniasAereas();
        modelo=new DefaultComboBoxModel<>();
        modelo.addAll(listaCompanias);
        comboCompania.setModel(modelo);
        comboCompania.setSelectedIndex(0);
        refrescarDatos();
    }
    private void refrescarDatos(){
        compania=(CompaniaAerea)comboCompania.getSelectedItem();
        listaVuelosDiarios=LogicaNegocio.getVuelosDiariosCompania((Date)spnFecha.getValue(), compania);
        listaVuelosBase=LogicaNegocio.getListaActualVuelosBase();
        if (listaVuelosDiarios!=null){
        modelotabla=new TableVuelosCompaniaModel(listaVuelosDiarios, listaVuelosBase);
        tabla.setModel(modelotabla);
        }else{
            JOptionPane.showMessageDialog(this, "No se muestran vuelos con fechas anteriores a la actual");
        }
    }
