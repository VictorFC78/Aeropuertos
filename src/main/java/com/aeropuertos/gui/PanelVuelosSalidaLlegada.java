    private void establecerTableModel(boolean tipoVuelo){
        listaVuelosDiarios=LogicaNegocio.getVuelosDiariosFecha((Date)spnFecha.getValue(), tipoVuelo);
        if(listaVuelosDiarios!=null){
            modelo=new TablePanelVuelosModel(listaVuelosDiarios,LogicaNegocio.getListaActualVuelosBase()
                    , tipoVuelo);
            tabla.setModel(modelo);
        }else{
            JOptionPane.showMessageDialog(this, "No se muestran vuelos con fechas anteriores a la actual");
        }
    }
