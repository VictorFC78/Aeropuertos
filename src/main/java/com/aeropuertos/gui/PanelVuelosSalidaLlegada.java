    private void btnTemperaturasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTemperaturasActionPerformed
         //si la fecha no es la actual entonces no permite ver las temperaturas
        int fechaCorrecta=ValidadorDatos.compararFechaActual((Date)spnFecha.getValue());
        if(fechaCorrecta!=0){
            JOptionPane.showMessageDialog(this, "Solo se muestran temperqaturas del dia actual");
            vueloDiario=null;
        }else{
            if(vueloDiario==null){
                JOptionPane.showMessageDialog(this, "No ha seleccionado vuelo");
            }else{
               PanelVuelosTemperaturas temperaturas=new PanelVuelosTemperaturas(this, true);
               temperaturas.setVisible(true);
            } 
        }
    }//GEN-LAST:event_btnTemperaturasActionPerformed

